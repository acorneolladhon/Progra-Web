// Declara una variable numérica y utiliza los operadores de incremento (++) y 
// decremento (--) para modificar su valor. Imprime el valor de la variable después 
// de cada operación.

let numero = 50

console.log("Valor inicial:", numero);

numero++;
console.log("Después de incrementar:", numero);

numero--;
console.log("Después de decrementar:", numero);